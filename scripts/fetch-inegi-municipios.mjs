#!/usr/bin/env node
/**
 * Fetches all MX municipios from INEGI Catálogo Único (public API)
 * and writes seed JSON + SQL migration fragment.
 *
 * Source: https://gaia.inegi.org.mx/wscatgeo/v2/mgem/{cve_ent}
 * INEGI Marco Geoestadístico — Áreas Geoestadísticas Municipales (AGEM)
 */

import { writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");

/** INEGI cve_ent (01–32) → geo_states.code in our catalog */
const CVE_ENT_TO_STATE_CODE = {
  "01": "AGU",
  "02": "BCN",
  "03": "BCS",
  "04": "CAM",
  "05": "COA",
  "06": "COL",
  "07": "CHP",
  "08": "CHH",
  "09": "CMX",
  "10": "DUR",
  "11": "GUA",
  "12": "GRO",
  "13": "HID",
  "14": "JAL",
  "15": "MEX",
  "16": "MIC",
  "17": "MOR",
  "18": "NAY",
  "19": "NLE",
  "20": "OAX",
  "21": "PUE",
  "22": "QUE",
  "23": "ROO",
  "24": "SLP",
  "25": "SIN",
  "26": "SON",
  "27": "TAB",
  "28": "TAM",
  "29": "TLA",
  "30": "VER",
  "31": "YUC",
  "32": "ZAC",
};

const INEGI_BASE = "https://gaia.inegi.org.mx/wscatgeo/v2/mgem";

function sqlEscape(value) {
  return String(value).replace(/\\/g, "\\\\").replace(/'/g, "''");
}

async function fetchStateMunicipios(cveEnt) {
  const url = `${INEGI_BASE}/${cveEnt}`;
  const res = await fetch(url, {
    headers: { Accept: "application/json" },
  });
  if (!res.ok) {
    throw new Error(`INEGI ${url} → HTTP ${res.status}`);
  }
  const body = await res.json();
  return body.datos ?? [];
}

async function main() {
  const municipios = [];

  for (const cveEnt of Object.keys(CVE_ENT_TO_STATE_CODE).sort()) {
    const stateCode = CVE_ENT_TO_STATE_CODE[cveEnt];
    process.stderr.write(`Fetching cve_ent=${cveEnt} (${stateCode})…\n`);
    const rows = await fetchStateMunicipios(cveEnt);
    for (const row of rows) {
      municipios.push({
        cvegeo: row.cvegeo,
        cve_ent: row.cve_ent,
        cve_mun: row.cve_mun,
        name: row.nomgeo,
        state_code: stateCode,
      });
    }
    await new Promise((r) => setTimeout(r, 120));
  }

  municipios.sort((a, b) => a.cvegeo.localeCompare(b.cvegeo));
  process.stderr.write(`Total municipios: ${municipios.length}\n`);

  const homonymKeys = new Map();
  for (const m of municipios) {
    const key = `${m.state_code}|${m.name}`;
    homonymKeys.set(key, (homonymKeys.get(key) ?? 0) + 1);
  }
  for (const m of municipios) {
    const key = `${m.state_code}|${m.name}`;
    if ((homonymKeys.get(key) ?? 0) > 1) {
      m.name = `${m.name} · ${parseInt(m.cve_mun, 10)}`;
    }
  }

  const seedDir = join(ROOT, "database", "seed");
  mkdirSync(seedDir, { recursive: true });

  const jsonPath = join(seedDir, "mx_municipios_inegi.json");
  writeFileSync(
    jsonPath,
    JSON.stringify(
      {
        source: "INEGI Marco Geoestadístico — Catálogo Único de Claves Geoestadísticas",
        api: INEGI_BASE,
        fetched_at: new Date().toISOString().slice(0, 10),
        count: municipios.length,
        municipios,
      },
      null,
      2,
    ),
    "utf8",
  );
  process.stderr.write(`Wrote ${jsonPath}\n`);

  const values = municipios
    .map(
      (m) =>
        `  SELECT '${sqlEscape(m.state_code)}' AS state_code, '${sqlEscape(m.name)}' AS name, '${sqlEscape(m.cvegeo)}' AS cvegeo, '${sqlEscape(m.cve_ent)}' AS cve_ent, '${sqlEscape(m.cve_mun)}' AS cve_mun`,
    )
    .join(" UNION ALL\n");

  const sql = `-- Auto-generated by scripts/fetch-inegi-municipios.mjs — do not edit by hand
-- Municipios: ${municipios.length} | Source: INEGI wscatgeo v2/mgem

INSERT INTO \`geo_cities\` (\`state_id\`, \`name\`, \`cvegeo\`, \`cve_ent\`, \`cve_mun\`, \`source\`, \`is_active\`)
SELECT s.id, m.name, m.cvegeo, m.cve_ent, m.cve_mun, 'inegi', 1
FROM (
${values}
) AS m
JOIN \`geo_states\` s ON s.country = 'MX' AND s.code COLLATE utf8mb4_unicode_ci = m.state_code COLLATE utf8mb4_unicode_ci
ON DUPLICATE KEY UPDATE
  \`name\` = VALUES(\`name\`),
  \`cvegeo\` = VALUES(\`cvegeo\`),
  \`cve_ent\` = VALUES(\`cve_ent\`),
  \`cve_mun\` = VALUES(\`cve_mun\`),
  \`source\` = IF(\`geo_cities\`.\`lat\` IS NOT NULL AND \`geo_cities\`.\`source\` = 'seed', 'seed', VALUES(\`source\`)),
  \`lat\` = COALESCE(\`geo_cities\`.\`lat\`, VALUES(\`lat\`)),
  \`lng\` = COALESCE(\`geo_cities\`.\`lng\`, VALUES(\`lng\`)),
  \`is_active\` = 1;
`;

  const sqlPath = join(ROOT, "database", "migrations", "20260613_140001_inegi_municipios_data.sql");
  writeFileSync(sqlPath, sql, "utf8");
  process.stderr.write(`Wrote ${sqlPath}\n`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
